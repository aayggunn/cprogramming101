#include <stdio.h>
#include <stdlib.h>

int main(void) {
	
	char myLetter = 'C';
	int myNumber = 7;
	float myNumber2 = 3.14;
	double myNumber3 = 987.20948;
	
	printf("%c\n", myLetter);
	printf("%d\n", myNumber);
	printf("%f\n", myNumber2);
	printf("%lf\n", myNumber3);
	printf("%e\n", myNumber3);
	
	return 0;
}
